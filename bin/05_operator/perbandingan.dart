void main() {
  // operator perbandingan hanya bernilai true dan false saja atau disebut boolean
  print(15 == 15); // true
  print(15 != 15); // false
  print(15 >= 15); // true
  print(15 <= 15); // true
  print(15 > 15); // false
  print(15 < 15); // false

  print('Dode' == 'Teddy'); // false
  print('Trisna' == 'Trisna'); // true
}
