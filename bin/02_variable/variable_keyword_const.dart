void main() {
  const nama = 'Dode Teddy';

  // nama = 'Trisnardiantari'; apabila di uncoment akan error

  print(nama);

  const angka = [1, 2, 3];

  // angka.add(4); berbeda dengan final mengubah nilai pada list masih bisa namun const tidak bisa sama sekali

  // angka = [6,7,8]; apabila di uncoment akan error

  print(angka);
}

/* 
keyword const digunakan apabila kita ingin membuat variable yang bersifat immutable (tidak dapat diubah bagaimanapun caranya) atau hanya bisa dihardcode
