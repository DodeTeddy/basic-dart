void main() {
  /* while loop sebenarnya sama dengan for loop bisa dikatan ini merupakan bentuk sederhana dari while loop
  dalam for loop terdapat init statement, kondisi dan post statement sedangkan dalam while hanya kondisi saja 
  */
  var value = 0;
  while (value < 3) {
    // apabila kondisi bernilai true maka baris kode didalam while akan terus dieksekusi
    print('Hello, World!');
    value++;
  }
}
